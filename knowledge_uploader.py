    def upload_knowledge(self, title, content, category, subject, difficulty_level="beginner", tags=None):
        """–ó–∞–≥—Ä—É–∑–∫–∞ –∑–Ω–∞–Ω–∏–π –≤ –±–∞–∑—É –¥–∞–Ω–Ω—ã—Ö"""
